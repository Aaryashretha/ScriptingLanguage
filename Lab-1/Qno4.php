<?php
// Function to calculate the sum of two numbers
function addNumbers($num1, $num2) {
    return $num1 + $num2; // Return the sum of the numbers
}

// Example usage
$number1 = 10;
$number2 = 20;

$result = addNumbers($number1, $number2); // Call the function with two numbers
echo "The sum of $number1 and $number2 is $result.\n";
?>